    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String macv = txtMaCV.getText();
        String tencv = txtTenCV.getText();
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa \"" + tencv + "\" khỏi danh sách không?", "Xác nhận", dialogButton);

        if (dialogResult == JOptionPane.YES_NO_OPTION) {
            int rowDeleted = ctr.get_Delete_ChucVu(macv);
            if (rowDeleted > 0) {
                JOptionPane.showMessageDialog(this, "Xóa chức vụ thành công");
                DataTable();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa chức vụ thất bại");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblChucVuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChucVuMouseClicked
        int row = tblChucVu.getSelectedRow();
        txtMaCV.setText(tblChucVu.getValueAt(row, 0).toString());
        txtTenCV.setText(tblChucVu.getValueAt(row, 1).toString());
        if (tblChucVu.getValueAt(row, 2).toString().trim().equals("Active")) {
            chbTrangThai.setSelected(true);
        } else {
            chbTrangThai.setSelected(false);
        }
    }//GEN-LAST:event_tblChucVuMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String macv = txtMaCV.getText().trim();
        String tencv = txtTenCV.getText();
        int trangthai;
        if (chbTrangThai.isSelected()) {
            trangthai = 1;
        } else {
            trangthai = 0;
        }

        if (macv.equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đủ thông tin, nhập lại!");
        } else if (tencv.equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đủ thông tin, nhập lại!");
        } else {
            switch (flag) {
                case 1:
                    int rowInserted = ctr.get_Insert_ChucVu(macv, tencv, trangthai);
                    if (rowInserted > 0) {
                        JOptionPane.showMessageDialog(this, "Thêm chức vụ thành công");
                        EnableECRUD();
                        DataTable();

                    } else {
                        JOptionPane.showMessageDialog(this, "Mã chức vụ đã tồn tại");
                    }
                    break;

                case 2:
                    int rowUpdated = ctr.get_Update_ChucVu(macv, tencv, trangthai);
                    if (rowUpdated > 0) {
                        JOptionPane.showMessageDialog(this, "Sửa chức vụ thành công");
                        EnableECRUD();
                        DataTable();

                    } else {
                        JOptionPane.showMessageDialog(this, "Sửa chức vụ thất bại!");
                    }
                    break;
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

