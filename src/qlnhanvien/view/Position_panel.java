    public final void DataTable() {
        String[] columns = {"Mã chức vụ", "Tên chức vụ", "Trạng thái"};
        model = new DefaultTableModel();
        model.setColumnIdentifiers(columns);
        tblChucVu.setModel(model);
        String macv = "";
        String tencv = "";
        String trangthai = "";

        try {
            int dem1 = 0, dem2 = 0;
            for (int i = 0; i < ctr.getDataTable().size(); i++) {

                macv = ctr.getDataTable().get(i).getMacv();
                tencv = ctr.getDataTable().get(i).getTencv();

                switch (String.valueOf(ctr.getDataTable().get(i).getTrangthai())) {
                    case "1":
                        trangthai = "Active";
                        dem1++;
                        break;
                    case "0":
                        trangthai = "Disactive";
                        dem2++;
                        break;

                }
                model.addRow(new Object[]{
                    macv, tencv, trangthai
                });
            }
        } catch (Exception e) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, e);
        }
    }

